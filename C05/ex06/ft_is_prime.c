int	ft_is_prime(int nb)
{
	int	div;

	if (nb < 2)
		return (0);
	div = 2;
	if (nb == 2 || nb == 3)
		return (1);
	while (div <= nb / div)
	{
		if ((nb % div++) == 0)
			return (0);
	}
	return (1);
}
